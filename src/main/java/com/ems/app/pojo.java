package com.itextpdf.text;
public class pojo{
	
	private String name;
	private String Designation;
	private String Department;
	private String SSN;
	private String CaliforniaID;
	
	public String getname()  {
		return name;
	}

	public void setname() {
		this.name= name;
	}

	public String getDesignation() {
		return Designation;
	}

	public void setDesignation() {
		this.Designation= Designation;
	}

	public String getDepartment() {
		return Department;
	}

	public void setDepartment() {
		this.Department = Department;
	}
	public String getSSN(){
		return SSN;
	}
	
	public void setSSN() {
		this.SSN = SSN;
	}
	
	public String getCaliforniaID() {
		return CaliforniaID;
	}
	
	public void setCaliforniaID(){
		this.CaliforniaID = CaliforniaID;
	}
}